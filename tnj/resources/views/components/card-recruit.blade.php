    <h3 class="text-2xl" id="recruitment">The Recruitment Funnel</h3>
    <p class="my-4">As a job seeker, it can feel like getting a new job is all about trying to 
        convince someone to take a chance on you, espeically if you're new to the field. 
        In fact, finding the right candidates can be enourmously difficult and 
        expensive for an employer. This is why any job seeker coaching or 
        interview coaching you may have access to will focus on how you bring value to 
        the employer, not what the employer can do for you. That said, often just 
        getting the interview can be the trickiest part of the job seekers journey.
        The <b>Recruitment Funnel</b> is a spin on the Conversion Funnel in that the 
        recruiter is trying to sell the organisation or the team within an organisation. 
        Much like the Conversion Funnel, you start with a wide net, you want as many applicants 
        as possible, gradually reducing the number of potential candidates down to a number 
        that you think will make it through to offer.</p>

