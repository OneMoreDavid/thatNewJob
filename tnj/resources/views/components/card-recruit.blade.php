    <p class="my-4">
        It starts with listing the job, using the places that are known to generate a 
        lot of leads. Once those applications start rolling in, you need to prioritise the 
        time your human workforce is spending on this activity, therefore the use of tools 
        to help automate things like <b>CV scanning</b> equate to using low cost 
        (relatively speaking) tools which is why a job seeker rarely gets a personalised 
        response to their application. The system automated your rejection and is unable to 
        offer any feedback of great value at this stage. Much like the Conversion Funnel 
        the aim is to whittle down the potential number of applicants down to a number 
        that is more manageable at the interview stage, with the best of the interviewees 
        receiving a job offer. 
